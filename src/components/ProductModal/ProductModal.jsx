const Modal = ({ produto, contato, onClose }) => {
  if (!produto) return null

  // Fecha o modal se clicar no fundo escuro
  const handleOverlayClick = (e) => {
    if (e.target === e.currentTarget) {
      onClose()
    }
  }

  // 1. Monta a mensagem (com o nome em negrito usando os asteriscos)
  const mensagem = `Olá! Tenho interesse no produto: *${produto.nome}*`;
  
  // 2. Codifica a mensagem para remover espaços e caracteres que quebram a URL
  const mensagemCodificada = encodeURIComponent(mensagem);

  // 3. Monta o link final (certifique-se que 'contato' é apenas números, ex: 5554991580756)
  const linkWhatsapp = `https://wa.me/${contato}?text=${mensagemCodificada}`;
  // ------------------------------------------

  return (
    <div className="modal-overlay" onClick={handleOverlayClick}>
      <div className="modal-conteudo">
        
        {/* Botão Fechar */}
        <button className="modal-fechar" onClick={onClose} aria-label="Fechar">
          <IconClose />
        </button>

        {/* Imagem (Container para evitar cortes) */}
        <div className="modal-imagem-container">
          <img 
            src={produto.imagem} 
            alt={produto.nome} 
            className="modal-imagem" 
          />
        </div>

        <div className="modal-info">
          <div className="modal-header">
            <h2>{produto.nome}</h2>
            {/* Só exibe a categoria se ela existir E não for vazia */}
            {produto.categoria && produto.categoria.trim() !== '' && (
              <span className="modal-categoria">{produto.categoria}</span>
            )}
          </div>

          {/* Descrição Principal */}
          <p className="modal-descricao">{produto.descricao}</p>

          {/* Detalhes Técnicos (Renderização Condicional) */}
          <div className="modal-detalhes">
            {produto.ano && (
              <p className="detalhe-item">
                <strong>Ano:</strong> {produto.ano}
              </p>
            )}
          </div>

          {/* CTA WhatsApp */}
          <div className="modal-cta">
            <p className="modal-cta-texto">
              Interessado? Entre em contato para consultar valores e condições.
            </p>
            
            {/* Link atualizado usando a variável que criamos */}
            <a 
              href={linkWhatsapp}
              target="_blank" 
              rel="noopener noreferrer"
              className="modal-btn-whatsapp"
            >
              <IconWhatsapp />
              Consultar Preço
            </a>
          </div>
        </div>
      </div>
    </div>
  )
}

export default Modal